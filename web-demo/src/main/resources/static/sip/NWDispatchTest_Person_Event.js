function OnPersonChanMsg(msg_id, msg_string, lParam, lParam_string, rParam, rParam_string) {
    if (msg_id == 100) {
        txtCallState.innerText = "有电话呼入"
    } else if (msg_id == 200) {
        txtCallState.innerText = "通话建立"
    } else if (msg_id == 300) {
        txtCallState.innerText = "通话断开"
    } else if (msg_id == 800) {
        txtCallState.innerText = "等待对端接听"
    }
}

